        public bool ValidaCPF(string cpf)
        {
            int multiplicador1 = 10;
            int multiplicador2 = 11;

            string tempCpf;
            string digito;
            int soma;
            int resto;

            cpf = cpf.Trim().Replace(".", "").Replace("-", "");
            if (cpf.Length != 11)
                return false;

            if (cpf == "11111111111")
                return false;


            if (cpf == "00000000000")
                return false;


            if (cpf == "33333333333")
                return false;

            if (cpf == "44444444444")
                return false;

            if (cpf == "55555555555")
                return false;


            if (cpf == "66666666666")
                return false;


            if (cpf == "77777777777")
                return false;


            if (cpf == "88888888888")
                return false;


            if (cpf == "99999999999")
                return false;

            tempCpf = cpf.Substring(0, 9);
            soma = 0;
            for (int i = 0; i < 9; i++)
                soma += int.Parse(tempCpf[i].ToString()) * (multiplicador1 - i);

            resto = soma % 11;
            if (resto < 2)
                resto = 0;
            else
                resto = 11 - resto;

            digito = resto.ToString();
            tempCpf = tempCpf + digito;
            soma = 0;

            for (int i = 0; i < 10; i++)
                soma += int.Parse(tempCpf[i].ToString()) * (multiplicador2 - i);

            resto = soma % 11;

            if (resto < 2)
                resto = 0;
            else
                resto = 11 - resto;

            digito = digito + resto.ToString();

            return cpf.EndsWith(digito);
        }
    }
